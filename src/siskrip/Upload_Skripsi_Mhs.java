    private void btnPilihFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihFileActionPerformed
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("PDF Files", "pdf");
        chooser.setFileFilter(filter);

        int result = chooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            selectedFile = chooser.getSelectedFile();
            txtNamaFile.setText(selectedFile.getName());
        }
    }//GEN-LAST:event_btnPilihFileActionPerformed

    private void btnKirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKirimActionPerformed
        if (selectedFile == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih file terlebih dahulu.");
            return;
        }

        try {
            // Simpan file ke folder 'uploads'
            String uploadFolder = "uploads/";
            File folder = new File(uploadFolder);
            if (!folder.exists()) folder.mkdirs();

            String newPath = uploadFolder + selectedFile.getName();
            Files.copy(selectedFile.toPath(), Paths.get(newPath), StandardCopyOption.REPLACE_EXISTING);

            // Simpan info ke database
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/siskrip", "root", "");
            String sql = "INSERT INTO dokumenskripsi (mahasiswa_id, judul_id, nama_file, url_file, tanggal_upload) " +
                         "VALUES (?, ?, ?, ?, CURDATE())";

            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Session.user_id);
            pst.setInt(2, getJudulId());
            pst.setString(3, selectedFile.getName());
            pst.setString(4, newPath);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "File berhasil diunggah!");
            txtNamaFile.setText("");
            selectedFile = null;

            pst.close();
            conn.close();
        } catch (IOException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengunggah file: " + e.getMessage());
        }
    }//GEN-LAST:event_btnKirimActionPerformed

