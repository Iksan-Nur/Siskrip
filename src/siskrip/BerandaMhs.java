    private void tampilkanDataMahasiswa() {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/siskrip", "root", "");
            String sql = "SELECT nama, nim_nidn FROM users WHERE nim_nidn = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, nim_nidn);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String nama = rs.getString("nama");
                NamaMhs.setText("HALO, " + nama.toUpperCase());
                NimMhs.setText("NIM : " + nim_nidn);
            }

            rs.close();
            pst.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }
    
    private void tampilkanStatusJudul() {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/siskrip", "root", "");
            String sql = "SELECT judul, status FROM judulskripsi WHERE mahasiswa_id = ? ORDER BY judul_id DESC LIMIT 1";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Session.user_id);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String judul = rs.getString("judul");
                String status = rs.getString("status");
                stsNamaJudul.setText(judul);
                stsJudul.setText(status);  // ‚Üê Label di GUI Anda
            } else {
                stsNamaJudul.setText("Belum Mengajukan");
                stsJudul.setText("Belum Mengajukan");
            }

            rs.close();
            pst.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat status judul: " + e.getMessage());
        }
    }
    
    private void tampilkanStatusSkripsi() {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/siskrip", "root", "");
            String sql = "SELECT nama_file, status FROM dokumenskripsi WHERE mahasiswa_id = ? ORDER BY dokumen_id DESC LIMIT 1";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Session.user_id);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String file = rs.getString("nama_file");
                String status = rs.getString("status");
                stsNamaFile.setText(file);
                stsSkripsi.setText(status);  // label di GUI Anda
            } else {
                stsNamaFile.setText("Belum Upload");
                stsSkripsi.setText("Belum Upload");
            }

            rs.close();
            pst.close();
            conn.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat status skripsi: " + e.getMessage());
        }

    }


