    private int getJudulId() throws SQLException {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/siskrip", "root", "");
        String sql = "SELECT judul_id FROM judulskripsi WHERE mahasiswa_id = ? ORDER BY judul_id DESC LIMIT 1";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, Session.user_id);
        ResultSet rs = pst.executeQuery();

        int id = 0;
        if (rs.next()) {
            id = rs.getInt("judul_id");
        }

        rs.close();
        pst.close();
        conn.close();
        return id;
    }

